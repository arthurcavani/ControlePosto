private void limparCampos() {
        txtId.setText("");
        txtNome.setText("");
    
        txtVlUnitario.setText("");
        
        spnQuantidade.setValue(0);
        txtNome.requestFocus();
    }

private void novoProduto() {
        limparCampos();
       txtNome.setEnabled(true);
        txtVlUnitario.setEnabled(true);
       
        spnQuantidade.setEnabled(true);
    }
