    private void btnSalvarbtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarbtnSalvarActionPerformed
       ProdutoConveniencia c = new ProdutoConveniencia();
        if (!txtId.getText().isEmpty()) {
            c.setId(Integer.parseInt(txtId.getText()));
        }
        c.setNome(txtNome.getText());        
        c.setQuantidade(Integer.parseInt(
                spnQuantidade.getValue().toString()));                
        c.setVlunitario(Double.parseDouble(
                txtVlUnitario.getText().toString()));       

        
        int res = -1;

        if (c.getId() == 0) {
            res = new ProdutoConvenienciaDAO().inserir(c);
            txtId.setText(String.valueOf(res));
        } else {
            res = new ProdutoConvenienciaDAO().atualizar(c);
        }

        if (res != -1) {
            JOptionPane.showMessageDialog(null,
                "Opera√ß√£o realizada com sucesso!",
                "Posto", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null,
                "Ocorreu um erro ao realizar a opera√ß√£o.",
                "Posto", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarbtnSalvarActionPerformed


