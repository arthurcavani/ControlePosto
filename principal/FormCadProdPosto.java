    private void btnSalvarbtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarbtnSalvarActionPerformed
          ProdutoPosto c = new ProdutoPosto();
        if (!txtId.getText().isEmpty()) {
            c.setId(Integer.parseInt(txtId.getText()));
        }
        c.setId_combustivel(Integer.parseInt(
                txtCombustivel.getText().toString()));
        c.setLitros(Integer.parseInt(
                spnLitros.getValue().toString()));      
       c.setVllitro(Double.parseDouble(
                txtVlUnitario.getText().toString()));
       c.setVltotal(Double.parseDouble(
                txtVlTotal.getText().toString())); 

        
        int res = -1;

        if (c.getId() == 0) {
            res = new ProdutoPostoDAO().inserir(c);
            txtId.setText(String.valueOf(res));
        } else {
            res = new ProdutoPostoDAO().atualizar(c);
        }

        if (res != -1) {
            JOptionPane.showMessageDialog(null,
                "Operação realizada com sucesso!",
                "Posto", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null,
                "Ocorreu um erro ao realizar a operação.",
                "Posto", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarbtnSalvarActionPerformed


