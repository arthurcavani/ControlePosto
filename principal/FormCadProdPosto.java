    private void limparCampos() {
        txtId.setText("");
        txtCombustivel.setText("");
        spnLitros.setValue(0);
        txtVlUnitario.setText("");
        txtVlTotal.setText("");
        
    }

    private void novoProduto() {
        limparCampos();
        txtCombustivel.setEnabled(true);
        spnLitros.setEnabled(true);
        txtVlUnitario.setEnabled(true);
        txtVlTotal.setEnabled(true); 
    }
    
    
    
    private void calcularValorTotal() {
        try {
            int qtde = Integer.parseInt(spnLitros.getValue().toString());
            double vlunit = Double.parseDouble(txtVlUnitario.getText());
            String res = String.valueOf(qtde * vlunit);
            txtVlTotal.setText(res);
        } catch (Exception e) {
            txtVlTotal.setText("");
        }
}
    
